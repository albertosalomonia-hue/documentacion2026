-- 6. Create notifications table
CREATE TABLE IF NOT EXISTS notifications (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL,
    type text NOT NULL,
    message text NOT NULL,
    actor_username text NOT NULL,
    is_read boolean DEFAULT false
);

-- 7. Enable RLS for notifications
ALTER TABLE notifications ENABLE ROW LEVEL SECURITY;

-- 8. Create policy for notifications
DROP POLICY IF EXISTS "Allow public access to notifications" ON notifications;

CREATE POLICY "Allow public access to notifications"
ON notifications
FOR ALL
USING (true)
WITH CHECK (true);

-- 9. Grant permissions for notifications
GRANT USAGE, SELECT ON ALL SEQUENCES IN SCHEMA public TO anon;
GRANT USAGE, SELECT ON ALL SEQUENCES IN SCHEMA public TO authenticated;
GRANT USAGE, SELECT ON ALL SEQUENCES IN SCHEMA public TO service_role;
