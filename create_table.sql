-- Run this in your Supabase SQL Editor to create the required table

-- 1. Create the table
CREATE TABLE IF NOT EXISTS accesook (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL,
    username text NOT NULL UNIQUE,
    password text NOT NULL,
    user_metadata jsonb DEFAULT '{}'::jsonb
);

-- 2. Enable RLS
ALTER TABLE accesook ENABLE ROW LEVEL SECURITY;

-- 3. Create policy for public access (since we use custom auth)
DROP POLICY IF EXISTS "Allow public access to accesook" ON accesook;

CREATE POLICY "Allow public access to accesook"
ON accesook
FOR ALL
USING (true)
WITH CHECK (true);

-- 4. Grant permissions
GRANT USAGE, SELECT ON ALL SEQUENCES IN SCHEMA public TO anon;
GRANT USAGE, SELECT ON ALL SEQUENCES IN SCHEMA public TO authenticated;
GRANT USAGE, SELECT ON ALL SEQUENCES IN SCHEMA public TO service_role;

